
public class hollow_plus_pattern  {
    public static void main(String[] args) {

        int n = 5;

        for (int i = 1; i <=n; i++) {
            if (i==3) {

                for (int j = 1; j <=2 ; j++) {
                    System.out.print(" "); 
                    
            }
                
            }else{
                System.out.print("* ");
            }
            System.out.println();
            
        }
        
    }
}
